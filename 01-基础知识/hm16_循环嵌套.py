"""
要求：
1) 一次只能打印1个*
2) 通过循环嵌套实现
3) 内循环控制一行几个*，外循环控制几行
* * * * *
* * * * *
* * * * *
* * * * *
* * * * *
* * * * *
"""

i = 0
while i <= 5:
    j = 0
    while j <= 5:
        print("*", end=" ")
        j += 1
    print()
    i += 1

"""
要求：
1) 一次只能打印1个*
2) 通过循环嵌套实现
3) 内循环控制一行几个*，判断条件<=外循环当前条件的值，外循环控制几行
* 
* *
* * * 
* * * * *
* * * * * *
* * * * * * *
"""
i = 0
while i <= 5:
    j = 0
    while j <= i:
        print("*", end=" ")
        j += 1
    print()
    i += 1
